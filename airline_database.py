    lines.append(f"- 유형: {data.get('type', 'N/A')}")
    lines.append(f"- 슬로건: {data.get('slogan', 'N/A')}")
    lines.append(f"- 비전: {data.get('vision', 'N/A')}")
    lines.append(f"- 미션: {data.get('mission', 'N/A')}")

    # 기본정보
    basic = data.get('basic_info', {})
    if basic:
        lines.append("\n### 기업 개요")
        for k, v in basic.items():
            lines.append(f"- {k}: {v}")

    # 핵심가치
    cv = data.get('core_values', [])
    if cv:
        lines.append("\n### 핵심가치")
        if isinstance(cv, dict):
            for k, v in cv.items():
                lines.append(f"- {k}: {v}")
        elif isinstance(cv, list):
            for v in cv:
                lines.append(f"- {v}")

    # 인재상
    talent = data.get('talent', {})
    if talent:
        lines.append("\n### 인재상")
        if isinstance(talent, dict):
            for k, v in talent.items():
                lines.append(f"- {k}: {v}")
        elif isinstance(talent, list):
            for v in talent:
                lines.append(f"- {v}")

    # 최근 이슈
    issues = data.get('recent_issues', [])
    if issues:
        lines.append("\n### 최근 이슈")
        for issue in issues:
            lines.append(f"- {issue}")

    # 채용 특징
    recruit = data.get('recruitment', {})
    if recruit:
        lines.append("\n### 채용 특징")
        if isinstance(recruit, dict):
            for k, v in recruit.items():
                lines.append(f"- {k}: {v}")

    # 키워드
    keywords = data.get('keywords', [])
    if keywords:
        lines.append(f"\n### 핵심 키워드: {', '.join(keywords)}")

