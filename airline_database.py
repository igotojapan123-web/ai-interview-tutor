    # 차별화 포인트
    diff = AIRLINE_DIFFERENTIATION.get(data.get('type', ''), {}).get(airline, '')
    if diff:
        lines.append(f"\n### 자소서 차별화 포인트: {diff}")

    return "\n".join(lines)
