def _is_soft_version(version: int) -> bool:
    """버전 번호로 SOFT/SHARP 여부 결정 (2,4,6=SOFT, 1,3,5=SHARP)"""
    return version % 2 == 0


def get_q1_pool_by_airline(airline: str, version: int = 2) -> List[str]:
    """항공사 유형(FSC/LCC/HSC)에 맞는 Q1 질문 풀 반환

    Args:
        airline: 항공사 이름
        version: 질문 버전 (1,3,5=SHARP 날카로움, 2,4,6=SOFT 부드러움)
    """
    atype = airline_profile(airline)
    is_soft = _is_soft_version(version)

    # 공통 풀 선택
    common_pool = Q1_POOL_COMMON_SOFT if is_soft else Q1_POOL_COMMON_SHARP

    # 항공사 유형별 풀 선택
    if atype == "FSC":
        type_pool = Q1_POOL_FSC_SOFT if is_soft else Q1_POOL_FSC_SHARP
    elif atype == "HSC":
        type_pool = Q1_POOL_HSC_SOFT if is_soft else Q1_POOL_HSC_SHARP
    else:  # LCC
        type_pool = Q1_POOL_LCC_SOFT if is_soft else Q1_POOL_LCC_SHARP

    return common_pool + type_pool


