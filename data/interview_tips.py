def get_tips_by_category(category: str) -> list:
    """카테고리별 팁 반환"""
    return [tip for tip in TIPS if tip["category"] == category]


def get_all_categories() -> list:
    """모든 카테고리 목록"""
    return list(set(tip["category"] for tip in TIPS))


def get_checklist(day: str) -> dict:
    """체크리스트 반환"""
    for tip in TIPS:
        if tip["category"] == "체크리스트" and day in tip["title"]:
            return tip
    return None
