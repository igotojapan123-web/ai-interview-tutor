# -*- coding: utf-8 -*-
"""
새 Q2 로직 테스트 스크립트
문항 의도 분석 → 핵심 문장 추출 → 질문 생성
"""

import os
import json
from openai import OpenAI

# API 키 로드
from dotenv import load_dotenv
load_dotenv()

client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

# 새로운 Q2 분석 프롬프트
Q2_ANALYSIS_PROMPT = """당신은 항공사 면접관입니다. 지원자의 자기소개서를 분석하여 면접 질문을 준비합니다.

## 자소서 문항
{question}

## 지원자 답변
{answer}

## 분석 순서 (반드시 이 순서로)

### 1단계: 문항 의도 분석
이 문항은 지원자의 무엇을 확인하려는 질문인가?
- 어떤 역량/가치관/경험을 보려고 하는가?
- 면접관이 궁금해할 핵심 포인트는?

### 2단계: 핵심 문장 3개 선정
문항 의도에 **직접 답하는** 핵심 문장을 답변에서 **정확히 원문 그대로** 추출하세요.
선정 기준 (우선순위):
1. 지원자의 **가치관/신념**이 드러나는 문장 (예: "~가 중요하다고 생각합니다", "~을 깨달았습니다")
2. 지원자가 **직접 행동한** 구체적 경험 (예: "~했습니다", "~를 조성하였습니다")
3. 지원자의 **깨달음/성장**이 드러나는 문장

주의:
- 단순 사실 진술(예: "16살에 유학을 갔습니다")은 피하세요
- 제목/이름/라벨(예: "영화 제목은 OOO입니다")은 핵심문장이 아님 - 제외하세요
- 핵심문장은 "왜?", "어떻게?"를 물을 수 있는 주장/경험/행동이어야 함
- 문장을 자르거나 수정하지 마세요
- 각 문장이 어떤 유형인지 분류: 가치주장 / 경험깨달음 / 구체적행동

### 3단계: 각 핵심 문장별 질문 포인트 3개
각 핵심 문장에 대해 3가지 각도의 **날카로운 면접 질문**을 준비하세요:
1. **구체화요구**: "구체적으로 어떤 상황이었나요?", "예를 들면?" (추상적 표현 파고들기)
2. **검증질문**: "정말 그렇게 했나요?", "그때 본인 역할이 뭐였나요?" (주장의 진위 확인)
3. **한계/반례**: "그 방법이 안 통하면?", "갈등이 생기면 어떻게 하시겠어요?" (약점 테스트)

질문 작성 시 주의:
- "풀어서 말씀해 주세요" 같은 약한 질문 금지
- 짧고 직접적으로 (1문장)
- 지원자가 구체적으로 답할 수 밖에 없는 질문

## JSON 출력 형식 (반드시 이 형식으로)
{{
  "question_intent": "문항이 확인하려는 핵심 (1-2문장)",
  "key_sentences": [
    {{
      "sentence": "핵심 문장 원문 (자르지 말 것)",
      "type": "가치주장/경험깨달음/구체적행동 중 하나",
      "why_important": "이 문장이 문항 의도에 부합하는 이유",
      "question_angles": [
        {{
          "angle": "구체화요구",
          "question": "면접 질문 (존댓말, 자연스럽게)"
        }},
        {{
          "angle": "검증질문",
          "question": "면접 질문"
        }},
        {{
          "angle": "한계/반례",
          "question": "면접 질문"
        }}
      ]
    }}
  ]
}}
"""

def test_new_q2_logic(question: str, answer: str) -> dict:
    """새 Q2 로직 테스트"""
    prompt = Q2_ANALYSIS_PROMPT.format(question=question, answer=answer)

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {"role": "system", "content": "당신은 항공사 면접관입니다. JSON 형식으로만 응답하세요."},
            {"role": "user", "content": prompt}
        ],
        temperature=0.7,
        response_format={"type": "json_object"}
    )

    result = json.loads(response.choices[0].message.content)
    return result


def print_result(result: dict):
    """결과를 보기 좋게 출력"""
    print("\n" + "="*60)
    print("[문항 의도 분석]")
    print("="*60)
    print(result.get("question_intent", "N/A"))

    print("\n" + "="*60)
    print("[핵심 문장 및 질문]")
    print("="*60)

    for i, ks in enumerate(result.get("key_sentences", []), 1):
        print(f"\n[핵심문장 {i}] ({ks.get('type', 'N/A')})")
        print(f"  \"{ks.get('sentence', 'N/A')}\"")
        print(f"  -> 선정 이유: {ks.get('why_important', 'N/A')}")

        print(f"\n  질문들:")
        for qa in ks.get("question_angles", []):
            print(f"    [{qa.get('angle', '')}]")
            print(f"    Q: {qa.get('question', 'N/A')}")
        print()


